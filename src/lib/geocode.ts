/**
 * Get coordinates for a city, with jitter to prevent overlapping markers
 */
export function getCoordinates(city: string, county: string, index: number): [number, number] {
  let coords: [number, number];

  if (OHIO_CITIES[city]) {
    coords = OHIO_CITIES[city];
  } else if (OHIO_COUNTIES[county]) {
    coords = OHIO_COUNTIES[county];
  } else {
    // Default to Ohio center
    coords = [40.4173, -82.9071];
  }

  // Add jitter to prevent overlapping markers
  const jitter = 0.008;
  const angle = (index * 137.5) % 360;
  const radius = (index % 5) * jitter * 0.3;

  return [
    coords[1] + Math.cos(angle * Math.PI / 180) * radius, // lng
    coords[0] + Math.sin(angle * Math.PI / 180) * radius, // lat
  ];
}
